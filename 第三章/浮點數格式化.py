# 設定欄位寬度與表示法
print(format(1234.5678,"10.2f")) # 欄位寬度為10字元，精確度為2位，浮點數表示法
print(format(1234.5678,"10.2e")) # 欄位寬度為10字元，精確度為2位，科學記號表示法
print(format(12,"10.2e")) # 欄位寬度為10字元，精確度為2位，浮點數表示法
print(format(8,"10.2%")) # 欄位寬度為10字元，精確度為2位，百分比表示法

# 設定對齊方式(數值預設靠右)與千分位符號
print(format(1234.5678,"<15.3f")) # 欄位寬度為15字元，精確度為3位，浮點數、靠左
print(format(1234.5678,"^15,.3f")) # 欄位寬度為15字元，精確度為3位，浮點數、靠左、置中、千分位符號
