
x=100 # 令變數X指向一個值為100的int物件
id(x) # 取得變數X指向之物件的id編號
print(id(x)) # 印出這個id編號

type(x) # 取得變數X指向之物件的型別
print(type(x)) # 印出這個型別

print(x) # 取得變數X指向之物件的值
# print(print(x)) 印出這個值 且等同於print(x)


