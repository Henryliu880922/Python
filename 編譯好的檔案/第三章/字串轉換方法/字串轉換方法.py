x="Hello,World"
print(str.upper(x)) # 所有字元轉換成大寫，亦可寫成x.upper()
print(str.lower(x)) # 所有字元轉換成小寫，亦可寫成x.lower()
print(str.swapcase(x)) # 大小寫互換
print(x.replace('World','Mary')) # 將World取代成Mary
print(str.capitalize("an egg")) # 第一個字元轉換成大寫，亦可寫成"an egg".capitalize()
print(str.title("an egg")) # 每個單字第一個字元轉換成大寫